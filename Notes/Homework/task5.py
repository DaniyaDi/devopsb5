#Write a Java program that reads a number in inches, converts it to meters.
#Note: One inch is 0.0254 meter.
#Test Data
#INCH: 2000
#Expected Output :
#2000.0 inch is 50.8 meters


# Formula 
#      1(inch) = 0.0254 (meters)
#      2000(inch) = x (meters)

x = 2000 * 0.0254 
print("2000 inch is", x, "meters")






